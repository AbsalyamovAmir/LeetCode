package MergeSortedLinkedLists;

public class ListNodeMergeLinkList {
    int val;
    ListNodeMergeLinkList next;

    public ListNodeMergeLinkList() {
    }

    public ListNodeMergeLinkList(int val) {
        this.val = val;
    }

    public ListNodeMergeLinkList(int val, ListNodeMergeLinkList next) {
        this.val = val;
        this.next = next;
    }
}
